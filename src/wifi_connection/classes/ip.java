package wifi_connection.classes;

public class ip {
	private String name;
	private byte[] address;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public byte[] getAddress() {
		return address;
	}
	public void setAddress(byte[] bs) {
		this.address = bs;
	}
}
